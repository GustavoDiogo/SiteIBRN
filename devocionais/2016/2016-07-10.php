                 “Contudo nós o consideramos castigado por Deus, por Deus atingido e afligido. Mas ele foi transpassado por causa das nossas transgressões, foi esmagado por causa de nossas iniquidades; o castigo que nos trouxe paz estava sobre ele, e pelas suas feridas fomos curados” Isaias 53.4b,5.
Alguém me contou que um lenhador saiu para a floresta a cortar a sua madeira. A esposa foi à cidade fazer compras, e no berço ficou uma criança de poucos meses de idade. Ambos, marido e mulher, iam voltar logo. Assim, não se preocuparam em deixar a criança sozinha.
O lenhador voltou primeiro. Entrando em casa encontrou os móveis revirados e tudo em desalinho. A um canto, o grande cão deitado, todo sujo de sangue. 0 homem, num instante, imaginou toda a cena que se teria desenrolado: “0 cachorro atacou o menino e o matou” - pensou. Sem pestanejar, tomou da espingarda e mirou na cabeça do pobre cachorro, e disparou.
Depois de ter matado o cachorro, correu para o quarto onde deveria estar a criança. Realmente ela ali estava. Estava viva e sorridente por ver o pai, depois do tremendo susto que deve ter passado. É que ao lado do seu berço jazia uma onça, a qual o cachorro matara em defesa da criança.<br><br>

Sola Cratia (Sua graça nos fez livres)!!!<br><br>

