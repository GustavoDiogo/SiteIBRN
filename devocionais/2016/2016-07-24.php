                 <h4>
                 “Não sobreveio a vocês tentação que não fosse comum aos homens. E Deus é fiel; ele não permitirá que vocês sejam tentados além do que podem suportar. Mas, quando forem tentados, ele mesmo lhes providenciará um escape, para que o possam suportar”.
 
Em 1876 na Inglaterra foi passada uma lei que obrigou todos os navios a terem uma marca pintada na lateral que indicava o seu limite de frete e carga. Quando o navio baixava na água até aquela marca, não era permitido que fosse embarcada mais mercadoria. O uso compulsório e obrigatório desta marca tornou as viagens marítimas daqueles dias muito mais seguras. À vista de Deus, cada um de nós tem uma marca semelhante. Os fardos e responsabilidades que Ele nos permite carregar podem parecer insuportáveis. Mas, Ele conhece os nossos limites, e não nos permitirá passar por mais do que temos condições de suportar
<br><br>

Sola Fide (Não vem de nós, é dom de Deus)!!!<br><br>

