                 <h4>
                 “Mas Jesus imediatamente lhes disse: “Coragem”! Sou eu. Não tenham medo!”.<br>
Conta-se que um homem esticou um cabo de aço sobre as Cataratas do Niágara, que ficam na divisa entre o Canadá e os Estados Unidos.Em seguida, atravessou andando sobre o cabo de aço. Foi e voltou.<br>
A multidão, extasiada, aplaudiu. Aproximando-se de uma senhora que estava no meio do povo, aquele homem perguntou:<br>
- A senhora acredita que eu consigo atravessar novamente?<br>
- É claro! - respondeu a mulher.<br>
- A senhora acredita que eu consigo atravessar levando um carrinho de mão?<br>
- Sim, eu acredito.<br>
- A senhora acredita que eu consigo atravessar levando um carrinho de mão com uma pessoa dentro?<br>
- Tenho certeza que sim!<br>
- Então, disse o homem, a senhora poderia, por favor, ajudar-me neste número, entrando no carrinho?<br>
- De jeito nenhum! <br>
- Ela acreditava, mas não confiava. <br><br>

Soli Deo Gloria (Glória a Deus somente)!!!<br><br>

