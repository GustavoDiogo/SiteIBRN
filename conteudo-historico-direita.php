<h4 class="centro">O nome Represart foi criado por um grupo anterior ao nosso,  na Igreja Batista em Represa Nova. Os anos se passaram e o Represart findou.<br />
  Quando Leonardo Dias e Lilian Gruenwadt idealizaram o projeto de peças autorais e releituras, com intuito evangelistico, assim como o grupo anterior e, ainda, em Represa Nova, os membros que fizeram parte do antigo projeto, nos apresentaram o Represart, então não vimos nome mais conveniente e chamoso, além de ser uma homenagem a todos que já participaram e queriam retornar.<br />
Em Novembro de 2012, demos início as produções de novas montagens teatrais, cantatas encenadas e musicais.<br />
Continuaremos em cena, até quando Deus, nosso pai, assim permitir.</h4>
