3º) Palavra de Deus: ensino da palavra de Deus, ebd, em casa, etc.<br>
4º) Intimidade: com Deus, entre nós e tudo isso será usado ao seu favor e te deixará mais forte para continuar.  E Nada do que fizer pagará o preço pago por você na cruz.<br>
Venha descobrir um pouco mais desse projeto! Você pode participar e também fazer parte das atividades com os jovens e adolescentes, visando sempre a glória de Deus e trabalhando para a Sua obra.
