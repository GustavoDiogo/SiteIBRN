                 <h4><center>"Enganosa é a beleza e vã a formosura, mas a mulher que teme ao Senhor, essa sim será louvada". Provérbios 31:30<br><br></center>
                 
                O ministério feminino da Represa Nova tem como propósito colaborar no fortalecimento da família e da Igreja na fé, no amor e na comunhão.<br>
Fortalecendo e apoiando umas às outras espiritualmente com oração, estudo da palavra de Deus, capacitando para a missão da família, esposa, mãe e filha.<br>
Nós reunimos onde louvamos a Deus, compartilhamos nossas experiências, testemunhamos o amor de Deus em nossas vidas, oramos e somos edificadas a cada encontro.<br>
Que nossas orações sejam exemplo de fé e que demonstre súplicas e petição que resultem na ação e no mover de Deus em nossas vidas.
