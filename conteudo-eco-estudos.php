                 <h4>Recente em nossos estudos nos lares fizemos uma análise sobre a vontade que o cristão tem em ajudar na igreja, de alguma forma, em algum ministério. Para aqueles que já frequentam a igreja regularmente essa é uma questão bem importante, e para aqueles que estão chegando e conhecendo mais sobre o amor de Deus com certeza também terão essa dúvida. Como posso ajudar? em qual mininstério eu me encaixo?
