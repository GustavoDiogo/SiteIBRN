Servindo de acordo com suas características pessoais<br><br>

Todos nós nos sentimos mais inclinados e atraídos para determinadas áreas de ministério em nossa igreja. Respeitar esta inclinação dentro de um equilíbrio estrutural faz parte do Serviço Autêntico. Como avaliar as inclinações que cada um tem para com o ministério? (1 Coríntios 1.6-9)<br>
I.	Levando-se em conta a paixão que cada um tem para com o       
ministério.<br>
1.	Que coisas ou atividades chamam mais a tua atenção na sua vida (meio social, político, econômico ou igreja?<br>
2.	Com que grupo de pessoas você se sente atraído?<br>
3.	Que área de necessidade é mais importante para você?<br>
4.	Que área de ministério de nossa igreja poderia contar com sua influência?<br><br>
II.      Você conhece qual é o seu temperamento?<br>
1.	Sanguíneo – Você se sente melhor quando está envolvido com pessoas?<br>
2.	Melancólico – Estar sozinho por longas horas não te inquieta?<br>
3.	Colérico – Gosta de tomar decisões?<br>
4.	Fleumático – Gosta de organizar coisas? <br><br>
III.	Quem são os beneficiados?<br>
A.	A obra do Senhor – Terá avivamento, crescimento em quantidade e qualidade.<br>
B.	O povo de Deus – Serão edificados e devidamente assistidos.<br>
C.      	Você mesmo (aquele que serve) – Crescerá na fé e sentir-se-á completo e realizado.<br><br>
Deus quer usar cada um de nós no serviço da sua obra. Quer usá-lo de maneira produtiva e eficaz, utilizando cada um dentro daquilo que Ele mesmo projetou para ser realizado. Nosso Deus é um Deus pessoal, que sabe lidar com a nossa personalidade e individualidade para o sucesso de sua obra no alcance da pluralidade de pessoas a quem Ele deseja salvar.<br><br>
