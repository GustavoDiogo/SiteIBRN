	<font color="black" face="arial">
		<p style="text-indent: 20px">
			A Igreja Batista Represa Nova entende que o seu diferencial, na verdade, é apenas a prática de uma ordenança de Jesus:
		</p>
		<div align="center">
			<i>“Ele respondeu: ‘Ame o Senhor, o seu Deus de todo o seu coração, de toda a sua alma, de todas as suas forças e de todo o seu entendimento’ e ‘Ame o seu próximo como a si mesmo’".</i>
			<div style="padding-bottom: 0.5em"></div>
			<b>(Lucas 10.27)</b>
		</div>
		<p style="text-indent: 20px">
			Ame o Senhor. O primeiro mandamento começa com um verbo de ação e decisão; é a ideia principal. Acima de todas as coisas que uma pessoa possa fazer para encontrar um sentido na vida, ela é chamada para uma decisão pessoal ao reconhecer que Cristo é salvação para todo o homem que Nele crer. Como igreja buscamos dependência em Deus para desenvolver nossa salvação com temor e tremor<b> (Filipenses 2.12)</b>. Nossa igreja busca colocar o nosso mestre Jesus Cristo no centro de todo louvor, serviço e devoção.
		</p>
		<div align="center">
			<i>"Um novo mandamento lhes dou: Amem-se uns aos outros. Como eu os amei, vocês devem amar-se uns aos outros.”</i> <b>(João 13.34)</b> 
		</div>
		<p style="text-indent: 20px">
			Nossa igreja também considera as necessidades do campo missionário hoje e quer honrar a ordem do “IDE e fazei discípulos de todas as nações”. Todo ser humano tem uma carência no âmago de seu ser: a necessidade de se relacionar com nosso DEUS criador. Por isso, Represa Nova incentiva pessoas para o preparo ministerial, isto é, comunicar às pessoas sobre as boas novas da salvação. É preciso levantar obreiros, pois <i>“a seara é grande, mas poucos são os ceifeiros”</i> <b>(Lucas 10.2)</b>, incentivar irmãos, apoiar seminaristas e enviar missionários. Ore para que o Senhor nos ajude com mais recursos, pois com isso preparamos a geração de agora para conduzir a igreja de amanhã.
		</p>
		<p style="text-indent: 20px">
			Outro recurso usado para comunicar a nova vida em Deus é o ministério de Teatro Represart. O teatro conta histórias com que nós nos identificamos, ensina, mobiliza, nos dá clareza e nos aproxima da mensagem do evangelho. Louvamos a Deus pelas peças desenvolvidas, pois têm sido usadas para o louvor da glória de Sua graça.
		</p>
